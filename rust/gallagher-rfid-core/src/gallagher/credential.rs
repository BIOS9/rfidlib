#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct GallagherCredential {
    pub region_code: u8,    // 4 bits (0–15)
    pub facility_code: u16, // 16 bits
    pub card_number: u32,   // 24 bits (0–16_777_215)
    pub issue_level: u8,    // 4 bits (0–15)
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum CredentialError {
    InvalidRegionCode(u8),
    InvalidCardNumber(u32),
    InvalidIssueLevel(u8),
}

impl fmt::Display for CredentialError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            CredentialError::InvalidRegionCode(v) => write!(f, "Invalid region code: {}", v),
            CredentialError::InvalidCardNumber(v) => write!(f, "Invalid card number: {}", v),
            CredentialError::InvalidIssueLevel(v) => write!(f, "Invalid issue level: {}", v),
        }
    }
}

impl GallagherCredential {
    pub fn new(
        region_code: u8,
        facility_code: u16,
        card_number: u32,
        issue_level: u8,
    ) -> Result<Self, CredentialError> {
        if region_code > 0x0F {
            return Err(CredentialError::InvalidRegionCode(region_code));
        }
        if card_number > 0xFF_FFFF {
            return Err(CredentialError::InvalidCardNumber(card_number));
        }
        if issue_level > 0x0F {
            return Err(CredentialError::InvalidIssueLevel(issue_level));
        }
        Ok(Self { region_code, facility_code, card_number, issue_level })
    }

    pub fn region_code_letter(&self) -> char {
        (b'A' + self.region_code) as char
    }

    pub fn decode(data: &[u8; 8]) -> Result<Self, CredentialError> {
        let s: [u8; 8] = core::array::from_fn(|i| DECODING_TABLE[data[i] as usize]);

        let region_code = (s[3] & 0x1F) >> 1;
        let facility_code = (((s[5] & 0x0F) as u16) << 12)
            | ((s[1] as u16) << 4)
            | (((s[7] & 0xF0) as u16) >> 4);
        let card_number = ((s[0] as u32) << 16)
            | (((s[4] & 0x1F) as u32) << 11)
            | ((s[2] as u32) << 3)
            | ((s[3] as u32) >> 5);
        let issue_level = s[7] & 0x0F;

        Self::new(region_code, facility_code, card_number, issue_level)
    }

    pub fn encode(&self) -> [u8; 8] {
        let arranged: [u8; 8] = [
            (self.card_number >> 16) as u8,
            (self.facility_code >> 4) as u8,
            ((self.card_number & 0x7FF) >> 3) as u8,
            (((self.card_number & 0x7) << 5) | ((self.region_code as u32) << 1)) as u8,
            ((self.card_number & 0xFFFF) >> 11) as u8,
            (self.facility_code >> 12) as u8,
            0,
            (((self.facility_code & 0xF) << 4) as u8) | self.issue_level,
        ];
        core::array::from_fn(|i| ENCODING_TABLE[arranged[i] as usize])
    }
}

impl fmt::Display for GallagherCredential {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(
            f,
            "RC={} ({}) FC={} CN={} IL={}",
            self.region_code,
            self.region_code_letter(),
            self.facility_code,
            self.card_number,
            self.issue_level,
        )
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn encode_decode_roundtrip() {
        let cred = GallagherCredential::new(3, 1234, 56789, 1).unwrap();
        let encoded = cred.encode();
        let decoded = GallagherCredential::decode(&encoded).unwrap();
        assert_eq!(cred, decoded);
    }

    #[test]
    fn invalid_region_code() {
        assert!(GallagherCredential::new(0x10, 0, 0, 0).is_err());
    }

    #[test]
    fn invalid_card_number() {
        assert!(GallagherCredential::new(0, 0, 0x1_000_000, 0).is_err());
    }

    #[test]
    fn invalid_issue_level() {
        assert!(GallagherCredential::new(0, 0, 0, 0x10).is_err());
    }

    #[test]
    fn encoding_table_is_inverse_of_decoding_table() {
        for i in 0usize..256 {
            let encoded = ENCODING_TABLE[i];
            let decoded = DECODING_TABLE[encoded as usize];
            assert_eq!(i as u8, decoded, "Round-trip failed at index {}", i);
        }
    }
}
